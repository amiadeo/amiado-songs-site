function route() {
  const hash = window.location.hash || '#/';
  const app = document.getElementById('app');

  // Update nav active state
  document.querySelectorAll('.nav-link').forEach(link => {
    link.classList.remove('active');
  });

  if (hash.startsWith('#/song/')) {
    const id = hash.slice(7);
    const song = SONGS.find(s => s.id === id);
    document.title = song ? `${song.title} — Amiado` : 'Amiado';
    document.querySelector('[data-route="home"]')?.classList.remove('active');
    app.innerHTML = renderSongPage(id);
  } else if (hash.startsWith('#/playlist/')) {
    const plId = hash.slice(11);
    document.title = 'Amiado';
    document.querySelector('[data-route="favorites"]')?.classList.add('active');
    app.innerHTML = renderPlaylistPage(plId);
  } else if (hash === '#/playlists') {
    document.title = 'Amiado';
    document.querySelector('[data-route="favorites"]')?.classList.add('active');
    app.innerHTML = renderPlaylistsPage();
  } else if (hash === '#/favorites') {
    document.title = 'Amiado';
    document.querySelector('[data-route="favorites"]')?.classList.add('active');
    app.innerHTML = renderFavoritesPage();
  } else if (hash === '#/bio') {
    document.title = 'Amiado';
    document.querySelector('[data-route="bio"]')?.classList.add('active');
    app.innerHTML = renderBioPage();
    setTimeout(runBioAnimation, 50);
  } else if (hash.startsWith('#/writing/')) {
    const id = hash.slice(10);
    document.title = 'Amiado';
    document.querySelector('[data-route="writings"]')?.classList.add('active');
    app.innerHTML = renderWritingPage(id);
  } else if (hash === '#/writings') {
    document.title = 'Amiado';
    document.querySelector('[data-route="writings"]')?.classList.add('active');
    app.innerHTML = renderWritingsPage();
  } else {
    document.title = 'Amiado';
    document.querySelector('[data-route="home"]')?.classList.add('active');
    app.innerHTML = renderHomePage();
    setTimeout(runHeroAnimation, 50);
  }

  app.querySelector('.page-enter') && void app.offsetHeight; // trigger animation
  window.scrollTo({ top: 0, behavior: 'instant' });
  bindPageEvents();
}

window.addEventListener('hashchange', route);
window.addEventListener('load', route);

