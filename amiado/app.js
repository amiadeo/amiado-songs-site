// Progress bar
audio.addEventListener('timeupdate', () => {
  if (!audio.duration) return;
  const pct = (audio.currentTime / audio.duration) * 100;
  document.getElementById('playerFill').style.width = pct + '%';
  document.getElementById('currentTime').textContent = fmtTime(audio.currentTime);
});

audio.addEventListener('loadedmetadata', () => {
  document.getElementById('totalTime').textContent = fmtTime(audio.duration);
});

document.getElementById('playerBar').addEventListener('click', (e) => {
  if (!audio.duration) return;
  const rect = e.currentTarget.getBoundingClientRect();
  const pct = (e.clientX - rect.left) / rect.width;
  audio.currentTime = pct * audio.duration;
});

document.getElementById('volumeSlider').addEventListener('input', (e) => {
  audio.volume = parseFloat(e.target.value);
});

function fmtTime(s) {
  const m = Math.floor(s / 60);
  const sec = Math.floor(s % 60).toString().padStart(2, '0');
  return `${m}:${sec}`;
}

