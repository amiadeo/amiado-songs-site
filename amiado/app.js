function bc(items) {
  // items: [{label, href}] — last item is current page (no link)
  const parts = items.map((item, i) => {
    const isLast = i === items.length - 1;
    return isLast
      ? `<span class="bc-item bc-current">${item.label}</span>`
      : `<a href="${item.href}" class="bc-item bc-link">${item.label}</a>`;
  });
  return `
    <nav class="breadcrumb" aria-label="ניווט">
      ${parts.join('<span class="bc-sep">›</span>')}
    </nav>`;
}

