function renderWritingPage(id) {
  const w = WRITINGS.find(x => x.id === id);
  if (!w) return `<div class="page-enter writing-page"><p>כתב לא נמצא.</p></div>`;

  const heroHTML = w.image
    ? `<div class="writing-hero">
        <img src="${w.image}" alt="${w.title}" class="writing-hero-img">
        <div class="writing-hero-overlay"></div>
        <div class="writing-hero-meta">
          ${bc([{label:'שירים',href:'#/'},{label:'כתבים',href:'#/writings'},{label:w.title,href:`#/writing/${w.id}`}])}
          <h1 class="writing-hero-title">${w.title}</h1>
        </div>
      </div>`
    : `<div class="writing-hero writing-hero--placeholder">
        <div class="writing-hero-overlay"></div>
        <div class="writing-hero-meta">
          ${bc([{label:'שירים',href:'#/'},{label:'כתבים',href:'#/writings'},{label:w.title,href:`#/writing/${w.id}`}])}
          <h1 class="writing-hero-title">${w.title}</h1>
        </div>
      </div>`;

  const cleanBody = w.body.split('\n').map(l => l.trim()).join('\n').trim();
  const isProse = w.type !== 'שיר';
  let bodyHTML;
  if (isProse) {
    bodyHTML = cleanBody.split('\n\n')
      .filter(p => p.trim())
      .map(p => `<p>${p.replace(/\n/g, '<br>')}</p>`).join('');
  } else {
    bodyHTML = `<div class="writing-poem">${cleanBody.split('\n\n')
      .filter(s => s.trim())
      .map(stanza =>
        `<div class="writing-stanza">${stanza.split('\n').filter(l => l.trim()).map(l => `<div class="writing-line">${l}</div>`).join('')}</div>`
      ).join('')}</div>`;
  }

  // Next writings gallery (up to 3 others, excluding current)
  const others = WRITINGS.filter(x => x.id !== w.id).slice(0, 3);
  const galleryCards = others.map(o => {
    const oImg = o.image
      ? `<img src="${o.image}" alt="${o.title}" style="width:100%;height:100%;object-fit:cover;display:block">`
      : `<div style="width:100%;height:100%;background:linear-gradient(135deg,#1a1e26,#232834)"></div>`;
    return `
      <a class="wnext-card" href="#/writing/${o.id}">
        <div class="wnext-card-img">${oImg}</div>
        <div class="wnext-card-title">${o.title}</div>
      </a>`;
  }).join('');

  return `
    <div class="page-enter writing-page">
      ${heroHTML}
      <div class="writing-content">
        <div class="writing-page-body">${bodyHTML}</div>
        <div class="wnext-section">
          <div class="wnext-heading">
            <a href="#/writings" class="wnext-all-link">לכל הכתבים <svg width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round"><polyline points="15 18 9 12 15 6"/></svg></a>
          </div>
          <div class="wnext-row">${galleryCards}</div>
        </div>
      </div>
    </div>`;
}

