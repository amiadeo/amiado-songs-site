const _scrollObserver = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('scroll-visible');
      _scrollObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.08, rootMargin: '0px 0px -40px 0px' });

function initScrollAnimations() {
  // Mark all elements as hidden first
  const singles = ['.songs-chapter-heading', '.song-credits', '.writing-page-body p', '.writing-stanza'];
  const staggered = ['.track-item', '.wcard', '.fav-card', '.pl-card', '.pl-track-item', '.wnext-card'];

  singles.forEach(sel => document.querySelectorAll(sel).forEach(el => el.classList.add('scroll-reveal')));
  staggered.forEach(sel => document.querySelectorAll(sel).forEach((el, i) => {
    el.classList.add('scroll-reveal');
    el.style.transitionDelay = `${i * 80}ms`;
  }));
  document.querySelectorAll('.analysis-card').forEach(el => el.classList.add('scroll-reveal'));

  // Wait for browser to paint the hidden state, then start observing
  setTimeout(() => {
    singles.forEach(sel => {
      document.querySelectorAll(sel).forEach(el => _scrollObserver.observe(el));
    });
    staggered.forEach(sel => {
      document.querySelectorAll(sel).forEach(el => _scrollObserver.observe(el));
    });

    // Song grid cards — row-by-row reveal (delay resets each time observer fires)
    const gridCardEls = document.querySelectorAll('.song-grid-card');
    if (gridCardEls.length) {
      const gridObserver = new IntersectionObserver((entries) => {
        let delay = 0;
        entries.forEach(entry => {
          if (!entry.isIntersecting) return;
          entry.target.style.transitionDelay = `${delay}ms`;
          delay += 80;
          entry.target.classList.add('scroll-visible');
          gridObserver.unobserve(entry.target);
        });
      }, { threshold: 0.08, rootMargin: '0px 0px -40px 0px' });
      gridCardEls.forEach(el => {
        el.classList.add('scroll-reveal');
        gridObserver.observe(el);
      });
    }

    // Analysis cards — cascade from heading
    const analysisHeading = document.querySelector('.analysis-heading');
    const analysisCards = document.querySelectorAll('.analysis-card');
    if (analysisHeading && analysisCards.length) {
      analysisHeading.classList.add('scroll-reveal');
      const headingObserver = new IntersectionObserver((entries) => {
        if (entries[0].isIntersecting) {
          entries[0].target.classList.add('scroll-visible');
          analysisCards.forEach((el, i) => {
            setTimeout(() => el.classList.add('scroll-visible'), 100 + i * 150);
          });
          headingObserver.disconnect();
        }
      }, { threshold: 0.5 });
      headingObserver.observe(analysisHeading);
    }
  }, 80);
}

