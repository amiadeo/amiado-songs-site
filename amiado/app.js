function getFavorites() {
  try { return JSON.parse(localStorage.getItem('amiado_favorites') || '[]'); } catch { return []; }
}
function saveFavorites(favs) { localStorage.setItem('amiado_favorites', JSON.stringify(favs)); }
function toggleFavorite(songId) {
  const favs = getFavorites();
  const idx = favs.indexOf(songId);
  if (idx >= 0) favs.splice(idx, 1); else favs.push(songId);
  saveFavorites(favs);
  return idx < 0; // true = added
}

function getPlaylists() {
  try { return JSON.parse(localStorage.getItem('amiado_playlists') || '[]'); } catch { return []; }
}
function savePlaylists(pls) { localStorage.setItem('amiado_playlists', JSON.stringify(pls)); }
function createPlaylist(name) {
  const pls = getPlaylists();
  const pl = { id: 'pl_' + Date.now(), name, songIds: [] };
  pls.push(pl);
  savePlaylists(pls);
  return pl;
}
function toggleSongInPlaylist(plId, songId) {
  const pls = getPlaylists();
  const pl = pls.find(p => p.id === plId);
  if (!pl) return;
  const idx = pl.songIds.indexOf(songId);
  if (idx >= 0) pl.songIds.splice(idx, 1); else pl.songIds.push(songId);
  savePlaylists(pls);
}

// Playlist dropdown
let _plDropdownSongId = null;
function openPlaylistDropdown(songId, anchorEl) {
  closePlaylistDropdown();
  _plDropdownSongId = songId;
  const pls = getPlaylists();
  const rect = anchorEl.getBoundingClientRect();

  const plItems = pls.map(pl => {
    const checked = pl.songIds.includes(songId) ? 'checked' : '';
    return `<label class="pl-dropdown-item">
      <input type="checkbox" ${checked} data-pl-id="${pl.id}">
      <span>${pl.name}</span>
    </label>`;
  }).join('');

  const dropdown = document.createElement('div');
  dropdown.id = 'plDropdown';
  dropdown.className = 'pl-dropdown';
  dropdown.innerHTML = `
    <div class="pl-dropdown-title">הוסף לפלייליסט</div>
    ${plItems}
    <div class="pl-dropdown-new" id="plNewForm">
      <button class="pl-dropdown-newbtn" id="plNewBtn">+ פלייליסט חדש</button>
      <div class="pl-dropdown-inputs" id="plNewInputs" style="display:none">
        <input class="pl-dropdown-input" id="plNewInput" placeholder="שם הפלייליסט..." dir="rtl">
        <button class="pl-dropdown-save" id="plNewSave">שמור</button>
      </div>
    </div>
  `;

  document.body.appendChild(dropdown);

  // Position below button — clamp so it stays within viewport
  const dropdownW = 200;
  const top = rect.bottom + window.scrollY + 6;
  const right = Math.max(8, Math.min(window.innerWidth - rect.right, window.innerWidth - dropdownW - 8));
  dropdown.style.top = top + 'px';
  dropdown.style.right = right + 'px';

  // Checkbox toggles
  dropdown.querySelectorAll('input[type=checkbox]').forEach(cb => {
    cb.addEventListener('change', () => {
      toggleSongInPlaylist(cb.dataset.plId, songId);
    });
  });

  // New playlist
  document.getElementById('plNewBtn').addEventListener('click', () => {
    document.getElementById('plNewBtn').style.display = 'none';
    document.getElementById('plNewInputs').style.display = 'flex';
    document.getElementById('plNewInput').focus();
  });
  document.getElementById('plNewSave').addEventListener('click', () => {
    const name = document.getElementById('plNewInput').value.trim();
    if (!name) return;
    const pl = createPlaylist(name);
    toggleSongInPlaylist(pl.id, songId);
    closePlaylistDropdown();
    openPlaylistDropdown(songId, anchorEl);
  });
  document.getElementById('plNewInput').addEventListener('keydown', e => {
    if (e.key === 'Enter') document.getElementById('plNewSave').click();
    if (e.key === 'Escape') closePlaylistDropdown();
  });

  // Close on outside click
  setTimeout(() => {
    document.addEventListener('click', _plOutsideClick);
  }, 10);
}
function _plOutsideClick(e) {
  const dd = document.getElementById('plDropdown');
  if (dd && !dd.contains(e.target)) closePlaylistDropdown();
}
function closePlaylistDropdown() {
  const dd = document.getElementById('plDropdown');
  if (dd) dd.remove();
  document.removeEventListener('click', _plOutsideClick);
  _plDropdownSongId = null;
}

// ─────────────────────────────────────────
// EVENT BINDING (after each render)
// ─────────────────────────────────────────
function bindPageEvents() {
  // Autoplay pending song after navigation to its page
  if (pendingAutoplaySongIdx >= 0) {
    const idx = pendingAutoplaySongIdx;
    pendingAutoplaySongIdx = -1;
    setTimeout(() => playSong(idx), 80);
  }

  // Song page nav buttons — auto-play the target song after navigation
  document.querySelectorAll('.song-hero-nav-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      const href = btn.getAttribute('href'); // e.g. "#/song/some-id"
      const songId = href.replace('#/song/', '');
      const idx = SONGS.findIndex(s => s.id === songId);
      if (idx >= 0) pendingAutoplaySongIdx = idx;
      // Navigation happens via the global click interceptor
    });
  });

  // Play buttons on song cards (click on card navigates, double-click plays)
  document.querySelectorAll('[data-play-idx]').forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.preventDefault();
      const idx = parseInt(btn.dataset.playIdx, 10);
      // Set navContext if playing from playlist detail page
      const songsList = btn.closest('.pl-tracklist');
      if (songsList && !btn.classList.contains('fav-card-play')) {
        const pl = getPlaylists().find(p => p.id === songsList.dataset.plId);
        if (pl) navContext = { type: 'playlist', ids: pl.songIds, plId: pl.id };
      }
      playSong(idx);
    });
  });

  // Card play overlay — clicking the play icon plays; clicking the card navigates (handled by <a>)
  document.querySelectorAll('.card-play-icon').forEach(icon => {
    const card = icon.closest('.song-card');
    icon.closest('.card-play-overlay').addEventListener('click', (e) => {
      e.preventDefault();
      e.stopPropagation();
      const idx = parseInt(card.dataset.idx, 10);
      playSong(idx);
      window.location.hash = `#/song/${SONGS[idx].id}`;
    });
  });

  // YouTube placeholder click
  document.querySelectorAll('.yt-placeholder').forEach(ph => {
    ph.addEventListener('click', () => {
      const songId = ph.id.replace('yt-ph-', '');
      const idx = SONGS.findIndex(s => s.id === songId);
      if (idx >= 0) playSong(idx);
    });
  });

  // Favorite buttons
  document.querySelectorAll('.track-fav-btn').forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.preventDefault();
      e.stopPropagation();
      const songId = btn.dataset.songId;
      const added = toggleFavorite(songId);
      btn.classList.toggle('active', added);
      const svg = btn.querySelector('svg');
      svg.setAttribute('fill', added ? 'currentColor' : 'none');
    });
  });

  // Favorites page play buttons — set navContext to favorites list
  document.querySelectorAll('.fav-card-play').forEach(btn => {
    btn.addEventListener('click', () => {
      const idx = parseInt(btn.dataset.playIdx, 10);
      if (!isNaN(idx)) {
        // Set navContext only if on favorites page (fav-card present)
        if (btn.closest('.fav-card')) {
          navContext = { type: 'favorites', ids: getFavorites() };
        } else if (btn.dataset.plId) {
          const pl = getPlaylists().find(p => p.id === btn.dataset.plId);
          if (pl) navContext = { type: 'playlist', ids: pl.songIds, plId: pl.id };
        }
        playSong(idx);
      }
    });
  });

  // Favorites page card — click card to navigate to song (set navContext)
  document.querySelectorAll('.fav-card[data-song-id]').forEach(card => {
    card.style.cursor = 'pointer';
    card.addEventListener('click', (e) => {
      if (e.target.closest('.fav-card-heart') || e.target.closest('.fav-card-play')) return;
      navContext = { type: 'favorites', ids: getFavorites() };
      window.location.hash = `#/song/${card.dataset.songId}`;
    });
  });

  // Favorites page heart — remove from favorites
  document.querySelectorAll('.fav-card-heart').forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.stopPropagation();
      toggleFavorite(btn.dataset.songId);
      // Remove the card from DOM without full re-render
      const card = btn.closest('.fav-card');
      if (card) {
        card.style.transition = 'opacity 0.25s, transform 0.25s';
        card.style.opacity = '0';
        card.style.transform = 'scale(0.9)';
        setTimeout(() => {
          card.remove();
          if (!document.querySelector('.fav-card')) {
            window.location.hash = '#/favorites'; // re-render empty state
          }
        }, 260);
      }
    });
  });

  // Playlist detail — clicking song title or open-btn sets navContext
  document.querySelectorAll('.pl-track-title-link[data-pl-id], .track-open-btn[data-pl-id]').forEach(el => {
    el.addEventListener('click', () => {
      const plId = el.dataset.plId;
      const pl = getPlaylists().find(p => p.id === plId);
      if (pl) navContext = { type: 'playlist', ids: pl.songIds, plId: pl.id };
    });
  });

  // Home page — Search filter
  const searchInput = document.getElementById('songsSearchInput');
  if (searchInput) {
    searchInput.addEventListener('input', () => {
      const q = searchInput.value.trim();
      const items = document.querySelectorAll('#songsTracklist .track-item, #songsGrid .song-grid-card');
      let visible = 0;
      items.forEach(item => {
        const match = !q || (item.dataset.title || '').includes(q);
        item.style.display = match ? '' : 'none';
        if (match) visible++;
      });
      const countEl = document.getElementById('songsSearchCount');
      if (countEl) countEl.textContent = q ? `${visible} שירים` : '';
    });
  }

  // Home page — Play All (sets navContext to all songs in order)
  const homePlayAll = document.getElementById('homePlayAllBtn');
  if (homePlayAll) {
    homePlayAll.addEventListener('click', (e) => {
      e.stopPropagation();
      navContext = { type: 'playlist', ids: SONGS.map(s => s.id), plId: null };
      playSong(0);
    });
  }

  // Home page — Shuffle
  const homeShuffle = document.getElementById('homeShuffleBtn');
  if (homeShuffle) {
    homeShuffle.addEventListener('click', () => {
      const shuffled = [...SONGS].sort(() => Math.random() - 0.5);
      navContext = { type: 'playlist', ids: shuffled.map(s => s.id), plId: null };
      homeShuffle.classList.add('active');
      playSong(SONGS.indexOf(shuffled[0]));
    });
  }

  // Home page — View toggle
  const songsViewList = document.getElementById('songsViewList');
  const songsViewGrid = document.getElementById('songsViewGrid');
  const songsTracklist = document.getElementById('songsTracklist');
  const songsGridEl = document.getElementById('songsGrid');
  const chapterLabel = document.getElementById('songsChapterLabel');
  function applySongsView(view) {
    if (!songsTracklist || !songsGridEl) return;
    if (view === 'grid') {
      songsTracklist.classList.add('hidden');
      songsGridEl.classList.remove('hidden');
      if (songsViewList) songsViewList.classList.remove('active');
      if (songsViewGrid) songsViewGrid.classList.add('active');
      if (chapterLabel) chapterLabel.innerHTML = 'רשימת השירים &nbsp;·&nbsp; תצוגת תמונות';
    } else {
      songsTracklist.classList.remove('hidden');
      songsGridEl.classList.add('hidden');
      if (songsViewList) songsViewList.classList.add('active');
      if (songsViewGrid) songsViewGrid.classList.remove('active');
      if (chapterLabel) chapterLabel.innerHTML = 'רשימת השירים &nbsp;·&nbsp; תצוגת רשימה';
    }
  }
  if (songsViewList) songsViewList.addEventListener('click', () => { localStorage.setItem('amiado_songs_view', 'list'); applySongsView('list'); });
  if (songsViewGrid) songsViewGrid.addEventListener('click', () => { localStorage.setItem('amiado_songs_view', 'grid'); applySongsView('grid'); });

  // Home page — Grid card click → navigate to song
  document.querySelectorAll('.song-grid-card[data-id]').forEach(card => {
    card.addEventListener('click', (e) => {
      if (e.target.closest('.song-grid-play-btn') || e.target.closest('.song-grid-add-btn') || e.target.closest('.song-grid-fav-btn')) return;
      navigateFade(`#/song/${card.dataset.id}`);
    });
  });

  // Home page — Grid card play
  document.querySelectorAll('.song-grid-play-btn').forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.preventDefault();
      e.stopPropagation();
      const idx = parseInt(btn.dataset.playIdx, 10);
      if (!isNaN(idx)) {
        navContext = { type: 'playlist', ids: SONGS.map(s => s.id), plId: null };
        playSong(idx);
      }
    });
  });

  // Playlist detail — Play All
  document.querySelectorAll('.pl-play-all-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      const plId = btn.dataset.plId;
      const pl = getPlaylists().find(p => p.id === plId);
      if (!pl) return;
      navContext = { type: 'playlist', ids: pl.songIds, plId: pl.id };
      const idx = parseInt(btn.dataset.playIdx, 10);
      if (!isNaN(idx)) playSong(idx);
    });
  });

  // Playlist detail — Shuffle
  document.querySelectorAll('.pl-shuffle-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      const plId = btn.dataset.plId;
      const pl = getPlaylists().find(p => p.id === plId);
      if (!pl || pl.songIds.length === 0) return;
      // Shuffle the ids
      const shuffled = [...pl.songIds].sort(() => Math.random() - 0.5);
      navContext = { type: 'playlist', ids: shuffled, plId: pl.id };
      btn.classList.add('active');
      // Play the first shuffled song
      const firstSong = SONGS.find(s => s.id === shuffled[0]);
      if (firstSong) {
        const idx = SONGS.indexOf(firstSong);
        playSong(idx);
      }
    });
  });

  // Playlist card — navigate to playlist page (but not when delete is clicked)
  document.querySelectorAll('.pl-card').forEach(card => {
    card.addEventListener('click', (e) => {
      if (e.target.closest('.pl-card-delete') || e.target.closest('.fav-card-play')) return;
      const plId = card.dataset.plId;
      if (plId) window.location.hash = `#/playlist/${plId}`;
    });
  });

  // Playlist detail — delete playlist
  document.querySelectorAll('.pl-detail-delete').forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.stopPropagation();
      const pls = getPlaylists().filter(p => p.id !== btn.dataset.plId);
      savePlaylists(pls);
      window.location.hash = '#/playlists';
    });
  });

  // Playlist detail — remove song from playlist
  document.querySelectorAll('.pl-track-remove').forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.stopPropagation();
      const { songId, plId } = btn.dataset;
      const pls = getPlaylists();
      const pl = pls.find(p => p.id === plId);
      if (!pl) return;
      pl.songIds = pl.songIds.filter(id => id !== songId);
      savePlaylists(pls);
      route();
    });
  });

  // Playlist detail — toggle add songs list
  document.querySelectorAll('.pl-add-songs-toggle').forEach(btn => {
    btn.addEventListener('click', () => {
      const list = btn.nextElementSibling;
      if (list) list.classList.toggle('hidden');
      btn.classList.toggle('active');
    });
  });

  // Playlist detail — add song to playlist
  document.querySelectorAll('.pl-add-song-btn').forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.stopPropagation();
      const { songId, plId } = btn.dataset;
      const pls = getPlaylists();
      const pl = pls.find(p => p.id === plId);
      if (!pl || pl.songIds.includes(songId)) return;
      pl.songIds.push(songId);
      savePlaylists(pls);
      route();
    });
  });

  // Playlist card delete buttons
  document.querySelectorAll('.pl-card-delete').forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.stopPropagation();
      const pls = getPlaylists().filter(p => p.id !== btn.dataset.plId);
      savePlaylists(pls);
      const isOnPlaylists = window.location.hash === '#/playlists';
      if (isOnPlaylists) {
        route(); // re-render same page
      } else {
        window.location.hash = '#/playlists';
      }
    });
  });

  // Playlist add buttons
  document.querySelectorAll('.track-add-btn').forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.preventDefault();
      e.stopPropagation();
      const songId = btn.dataset.songId;
      const dd = document.getElementById('plDropdown');
      if (dd && _plDropdownSongId === songId) {
        closePlaylistDropdown();
      } else {
        openPlaylistDropdown(songId, btn);
      }
    });
  });

  // Bio slider
  const slider = document.getElementById('bioSlider');
  if (slider) {
    let current = 0;
    const slides = slider.querySelectorAll('.bio-slide');
    const dots = slider.querySelectorAll('.bio-dot');

    function goToSlide(n) {
      slides[current].classList.remove('bio-slide--active');
      dots[current].classList.remove('bio-dot--active');
      current = (n + slides.length) % slides.length;
      slides[current].classList.add('bio-slide--active');
      dots[current].classList.add('bio-dot--active');
    }

    dots.forEach(dot => {
      dot.addEventListener('click', () => goToSlide(+dot.dataset.slide));
    });

    // Clear any previous interval when page re-renders
    if (window._bioSliderInterval) clearInterval(window._bioSliderInterval);
    window._bioSliderInterval = setInterval(() => goToSlide(current + 1), 4000);
  }

  // Scroll animations — run after DOM is ready
  requestAnimationFrame(initScrollAnimations);
}
